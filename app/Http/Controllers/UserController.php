     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user=User::findOrFail($id);
        $user_array=$request->get('user');
        if($user_array["password"] != $user_array["confirm_password"]){
            flash('As passwords nÃ£o coincidem')->important()->error();
        } else {
            $user->update([
                'name' => $user_array["name"],
                'email' => $user_array["email"],
                'password' => bcrypt($user_array["password"]),
            ]);
            flash('Utilizador Atualizado Com Sucesso!!!')->important()->success();
        }
        return redirect()->back();
    }

